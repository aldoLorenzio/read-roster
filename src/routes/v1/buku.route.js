const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const bukuValidation = require('../../validations/buku.validation');
const bukuController = require('../../controllers/buku.controller');

const router = express.Router();

router
  .route('/')
  .post(auth('admin'), validate(bukuValidation.createBuku), bukuController.createBuku)
  .get(auth('admin'), validate(bukuValidation.getBukus), bukuController.getBukus);

router
  .route('/detail/:bukuId')
  .get(bukuController.detailBuku);

router
  .route('/create')
  .get(bukuController.createView);

router
  .route('/:bukuId')
  .get(auth('admin'), validate(bukuValidation.getBuku), bukuController.getBuku)
  .patch(auth('admin'), validate(bukuValidation.updateBuku), bukuController.updateBuku)
  .delete(auth('admin'), validate(bukuValidation.deleteBuku), bukuController.deleteBuku);

module.exports = router;

